package datatype;

public class 字符char {
    public static void main(String[] args) {
        //数字与字符对照关系，ascii表
        char a ='1';
        System.out.println(a);
        int num = a; // from small --> large ,49
        System.out.println(num);
        char name ='中'; //单引号字符char,双引号字符串，字符加在一起叫串
        int number = name;
        System.out.println(number); // 20013
        // unicode 万国码
    }
}
